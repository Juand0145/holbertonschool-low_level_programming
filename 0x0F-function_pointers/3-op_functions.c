#include "3-calc.h"

/**
 * op_add - Sum of two Numbers
 * @a: first number
 * @b: second number
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substract two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two numbers
 * @a: first number
 * @b: second number
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the residuo of a division
 * @a: number
 * @b: number
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
